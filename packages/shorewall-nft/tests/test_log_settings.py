# ── 7. unknown LOG_BACKEND raises at build_ir time ───────────────────────────


class TestLogBackendUnknownRaises:
    """An unrecognised LOG_BACKEND value raises ValueError at build_ir()."""

    def test_unknown_backend_raises(self):
        config = load_config(MINIMAL_DIR)
        config.settings["LOG_BACKEND"] = "invalid"
        with pytest.raises(ValueError, match="invalid"):
            build_ir(config)

    def test_error_message_names_accepted_values(self):
        config = load_config(MINIMAL_DIR)
        config.settings["LOG_BACKEND"] = "IPTABLES"
        with pytest.raises(ValueError) as exc_info:
            build_ir(config)
        msg = str(exc_info.value)
        assert "IPTABLES" in msg
        # Message should hint at valid values.
        assert "LOG" in msg

    @pytest.mark.parametrize("bad", ["syslog", "kernel", "", "  "])
    def test_various_bad_backends(self, bad):
        config = load_config(MINIMAL_DIR)
        config.settings["LOG_BACKEND"] = bad
        with pytest.raises(ValueError):
            build_ir(config)


