class TestMinimalHappyPath:
    def test_returns_dict_of_tables(self, tmp_path):
        tables = parse_iptables_save(write_dump(tmp_path, MINIMAL_FILTER))
        assert isinstance(tables, dict)
        assert "filter" in tables

    def test_table_is_Table_instance(self, tmp_path):
        tables = parse_iptables_save(write_dump(tmp_path, MINIMAL_FILTER))
        assert isinstance(tables["filter"], Table)

    def test_single_rule_parsed(self, tmp_path):
        tables = parse_iptables_save(write_dump(tmp_path, MINIMAL_FILTER))
        rules = tables["filter"].rules["INPUT"]
        assert len(rules) == 1
        assert isinstance(rules[0], Rule)
        assert rules[0].target == "ACCEPT"

    def test_rule_raw_preserved(self, tmp_path):
        tables = parse_iptables_save(write_dump(tmp_path, MINIMAL_FILTER))
        rule = tables["filter"].rules["INPUT"][0]
        assert rule.raw == "-A INPUT -j ACCEPT"


