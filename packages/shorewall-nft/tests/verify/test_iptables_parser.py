class TestCommitMarker:
    def test_commit_does_not_produce_a_rule(self, tmp_path):
        tables = parse_iptables_save(write_dump(tmp_path, MINIMAL_FILTER))
        # If COMMIT were parsed as a rule, INPUT would have 2 entries or
        # there would be a stray 'COMMIT' chain.
        assert "COMMIT" not in tables["filter"].rules
        assert len(tables["filter"].rules["INPUT"]) == 1

    def test_commit_resets_table_context(self, tmp_path):
        # Rules after COMMIT but before the next *table marker should be
        # silently ignored (current is set to None by COMMIT).
        dump = """\
*filter
:INPUT ACCEPT [0:0]
-A INPUT -j ACCEPT
COMMIT
-A INPUT -j DROP
"""
        tables = parse_iptables_save(write_dump(tmp_path, dump))
        # The stray -A line after COMMIT must not be parsed.
        assert len(tables["filter"].rules["INPUT"]) == 1

    def test_multi_table_commit_separates_tables(self, tmp_path):
        tables = parse_iptables_save(write_dump(tmp_path, MULTI_TABLE))
        # filter rules must not bleed into nat and vice versa.
        assert "PREROUTING" not in tables["filter"].rules
        assert "INPUT" not in tables["nat"].rules


class TestGotoFlag:
    def test_goto_target_parsed(self, tmp_path):
        # -g TARGET is a "goto" (non-returning jump); we treat it the same
        # as -j for target classification purposes.
        tables = parse_iptables_save(write_dump(tmp_path, GOTO_RULE))
        rule = tables["filter"].rules["INPUT"][0]
        assert rule.target == "log_drop"


class TestMalformedInput:
    def test_missing_file_raises_file_not_found(self, tmp_path):
        with pytest.raises(FileNotFoundError):
            parse_iptables_save(tmp_path / "nonexistent.txt")

    def test_rule_outside_table_ignored(self, tmp_path):
        # -A lines that appear before any *table header have no current
        # table; they should be silently skipped.
        dump = """\
-A INPUT -j ACCEPT
*filter
:INPUT ACCEPT [0:0]
COMMIT
"""
        tables = parse_iptables_save(write_dump(tmp_path, dump))
        assert tables["filter"].rules["INPUT"] == []

    def test_empty_file_returns_empty_dict(self, tmp_path):
        tables = parse_iptables_save(write_dump(tmp_path, ""))
        assert tables == {}

    def test_only_comments_and_blanks_returns_empty(self, tmp_path):
        dump = """\
# just a comment

# another comment
"""
        tables = parse_iptables_save(write_dump(tmp_path, dump))
        assert tables == {}
