class TestNegation:
    def test_negated_iif_rule_is_parsed(self, tmp_path):
        # `-A FORWARD ! -i eth0 -j ACCEPT` — the "!" precedes the flag.
        # shlex splits this as ['!', '-i', 'eth0', ...].  The parser skips
        # the standalone '!' token (not in _FLAG_TO_KEY, not -m/-j/-g) and
        # then reads -i with value 'eth0' — so the negation marker is lost.
        # This is the current documented behaviour; the raw line still
        # preserves it for diagnostic use.
        tables = parse_iptables_save(write_dump(tmp_path, NEGATION_RULE))
        rule = tables["filter"].rules["FORWARD"][0]
        assert rule.iif is not None

    def test_negated_iif_raw_line_preserved(self, tmp_path):
        # Even though the negation is lost from matches, the raw line keeps it.
        tables = parse_iptables_save(write_dump(tmp_path, NEGATION_RULE))
        rule = tables["filter"].rules["FORWARD"][0]
        assert "!" in rule.raw

    def test_post_flag_negation_stored_with_bang(self, tmp_path):
        # When negation appears AFTER the flag ("-i ! eth0"), the parser
        # detects the "!" as the value token and prepends it: "!eth0".
        dump = """\
*filter
:FORWARD ACCEPT [0:0]
-A FORWARD -i ! eth0 -j ACCEPT
COMMIT
"""
        tables = parse_iptables_save(write_dump(tmp_path, dump))
        rule = tables["filter"].rules["FORWARD"][0]
        assert rule.iif == "!eth0"


